#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long s;

	s = n;
	if (s < 0)
	{
		ft_putchar_fd('-', fd);
		s = -1 * s;
	}
	if (s > 9)
		ft_putnbr_fd(s / 10, fd);
	ft_putchar_fd(s % 10 + '0', fd);
}
